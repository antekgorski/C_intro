#include <unistd.h>

void	print_solution(int **grid)
{
	int		m;
	int		n;
	char	c;

	m = 0;
	while (m < 4)
	{
		n = 0;
		while (n < 4)
		{
			c = grid[m][n] + '0';
			write(1, &c, 1);
			write(1, " ", 1);
			n++;
		}
		write(1, "\n", 1);
		m++;
	}
}
